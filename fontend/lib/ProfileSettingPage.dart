              throw Exception('‡∏£‡∏´‡∏±‡∏™‡∏ú‡πà‡∏≤‡∏ô‡∏õ‡∏±‡∏à‡∏à‡∏∏‡∏ö‡∏±‡∏ô‡πÑ‡∏°‡πà‡∏ñ‡∏π‡∏Å‡∏ï‡πâ‡∏≠‡∏á');
            }

            await Supabase.instance.client
                .from('users')
                .update({'password': _newPasswordController.text, 'updated_at': DateTime.now().toIso8601String()})
                .eq('id', widget.currentUser.id);
            debugPrint('‚úÖ ‡∏≠‡∏±‡∏û‡πÄ‡∏î‡∏ó‡∏£‡∏´‡∏±‡∏™‡∏ú‡πà‡∏≤‡∏ô‡∏î‡πâ‡∏ß‡∏¢ Supabase ‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à');
          }

        } catch (supabaseError) {
          debugPrint('‚ùå Supabase update failed: $supabaseError');
          // Fallback ‡πÑ‡∏õ‡πÉ‡∏ä‡πâ UserDatabase ‡πÅ‡∏ö‡∏ö‡πÄ‡∏Å‡πà‡∏≤
          throw supabaseError;
        }
      } else {
        // ‡πÉ‡∏ä‡πâ UserDatabase ‡πÅ‡∏ö‡∏ö‡πÄ‡∏Å‡πà‡∏≤ (fallback)
        debugPrint('üîÑ ‡πÉ‡∏ä‡πâ UserDatabase ‡πÅ‡∏ö‡∏ö‡πÄ‡∏Å‡πà‡∏≤ (fallback)');
        
        // ‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï‡∏ä‡∏∑‡πà‡∏≠
        if (_nameController.text.trim() != widget.currentUser.name) {
          newName = _nameController.text.trim();
          await UserDatabase.updateUser(
            id: widget.currentUser.id,
            name: newName,
          );
        }

        // ‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï‡∏£‡∏´‡∏±‡∏™‡∏ú‡πà‡∏≤‡∏ô (‡∏ñ‡πâ‡∏≤‡∏°‡∏µ‡∏Å‡∏≤‡∏£‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô)
        if (_showPasswordFields && _newPasswordController.text.isNotEmpty) {
          // ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏£‡∏´‡∏±‡∏™‡∏ú‡πà‡∏≤‡∏ô‡∏õ‡∏±‡∏à‡∏à‡∏∏‡∏ö‡∏±‡∏ô
          if (_currentPasswordController.text != widget.currentUser.password) {
            throw Exception('‡∏£‡∏´‡∏±‡∏™‡∏ú‡πà‡∏≤‡∏ô‡∏õ‡∏±‡∏à‡∏à‡∏∏‡∏ö‡∏±‡∏ô‡πÑ‡∏°‡πà‡∏ñ‡∏π‡∏Å‡∏ï‡πâ‡∏≠‡∏á');
          }

          await UserDatabase.updateUser(
            id: widget.currentUser.id,
            password: _newPasswordController.text,
          );
        }
      }

      // ‡∏à‡∏±‡∏î‡∏Å‡∏≤‡∏£‡∏£‡∏π‡∏õ‡∏†‡∏≤‡∏û (‡∏ñ‡πâ‡∏≤‡∏°‡∏µ‡∏Å‡∏≤‡∏£‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô)
      String? newImagePath = widget.currentUser.profileImagePath;
      if (_selectedImage != null) {
        try {
          // ‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏£‡∏π‡∏õ‡∏†‡∏≤‡∏û‡πÉ‡∏´‡∏°‡πà
          newImagePath = await ProfileImageService.updateProfileImage(
              _selectedImage!,
              widget.currentUser.id,
              widget.currentUser.profileImagePath);
          debugPrint('‚úÖ Profile image updated successfully: $newImagePath');
        } catch (e) {
          debugPrint('‚ùå Error updating profile image: $e');
          // ‡πÑ‡∏°‡πà‡∏ï‡πâ‡∏≠‡∏á throw exception ‡πÉ‡∏´‡πâ‡∏ó‡∏≥‡∏á‡∏≤‡∏ô‡∏ï‡πà‡∏≠‡πÑ‡∏î‡πâ
        }
      }

      // ‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï SharedPreferences ‡∏î‡πâ‡∏ß‡∏¢‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÉ‡∏´‡∏°‡πà
      final prefs = await SharedPreferences.getInstance();
      await prefs.setString('user_name', newName);
      await prefs.setString('name', newName); // ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö _loadProfile
      await prefs.setString(
          'user_profile_image', newImagePath ?? ''); // ‡πÄ‡∏û‡∏¥‡πà‡∏° path ‡∏£‡∏π‡∏õ‡∏†‡∏≤‡∏û

      debugPrint('‚úÖ Updated SharedPreferences with new name: $newName');
      if (newImagePath != null) {
        debugPrint('‚úÖ Updated SharedPreferences with profile image: $newImagePath');
      }

      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          const SnackBar(
            content: Text('‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï‡πÇ‡∏õ‡∏£‡πÑ‡∏ü‡∏•‡πå‡πÄ‡∏£‡∏µ‡∏¢‡∏ö‡∏£‡πâ‡∏≠‡∏¢‡πÅ‡∏•‡πâ‡∏ß'),
            backgroundColor: Colors.green,
          ),
        );

        // ‡∏Å‡∏•‡∏±‡∏ö‡πÑ‡∏õ‡∏´‡∏ô‡πâ‡∏≤ ProfilePage ‡∏û‡∏£‡πâ‡∏≠‡∏°‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÉ‡∏´‡∏°‡πà
        Navigator.pop(context, true);
      }
    } catch (e) {
      debugPrint('‚ùå Profile update error: $e');
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text('‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î: $e'),
            backgroundColor: Colors.red,
          ),
        );
      }
    } finally {
      if (mounted) {
        setState(() => _isLoading = false);
      }
    }
  }

  /// ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏Ñ‡∏ß‡∏≤‡∏°‡πÅ‡∏Ç‡πá‡∏á‡πÅ‡∏£‡∏á‡∏Ç‡∏≠‡∏á‡∏£‡∏´‡∏±‡∏™‡∏ú‡πà‡∏≤‡∏ô
  String? _validatePassword(String? value) {
    if (value == null || value.isEmpty) {
      return '‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡∏Å‡∏£‡∏≠‡∏Å‡∏£‡∏´‡∏±‡∏™‡∏ú‡πà‡∏≤‡∏ô';
